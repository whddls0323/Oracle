select a.acc_no,a.acc_cid,a.acc_name,c.c_name,a.acc_balance + 
sum(case when t.tr_type=001 then t.tr_amount else 0 end) - 
sum(case when t.tr_type=002 then t.tr_amount else 0 end) as 최종잔액
from account a left join transaction t on a.acc_no = t.tr_acc
join customer c on a.acc_cid=c.c_id where c_type=1
group by a.acc_no,a.acc_cid,a.acc_name,a.acc_balance,c.c_name order by a.acc_no;

